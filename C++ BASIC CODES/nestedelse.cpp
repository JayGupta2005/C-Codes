#include<iostream>
using namespace std;
int main (){
    char ch;
    cout<<"Enter Char between A to E";
    cin>>ch;
    if(ch=='a'){
        cout<<"Hello";
    }
    else if(ch=='b'){
        cout<<"Namaste";
    }
    else if(ch=='c'){
        cout<<"Ram Ram";
    }
    else if(ch=='d'){
        cout<<"Good Morning";
    }
    else if(ch=='e'){
        cout<<"Thankyou";
    }
}